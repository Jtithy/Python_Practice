# Creating a dictionary to store marks of subjects
marks = {"Math": 95, "Science": 90, "English": 88}
print("Marks Dictionary:", marks)

# Accessing marks subject wise
print("Math:", marks["Math"])
print("Science:", marks["Science"])
print("English:", marks["English"])
