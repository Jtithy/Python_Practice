# Printing numbers from 0 to 4 using a for loop
print("For Loop from 0 to 4:")
for item in range(5): # 0 to 4
    print(item)

# Printing numbers from 4 to 0 using a for loop
print("For Loop from 4 to 0:")
for item in range(4, -1, -1): # 4 to 0
    print(item)