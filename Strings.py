name = "Levi Ackermann"

print(name.upper()) # Convert to uppercase
print(name.lower()) # Convert to lowercase
print(name.find("Acker")) # Find substring
print("Acker" in name) # Check substring presence
print(name.replace("Levi", "Mikasa")) # Replace substring