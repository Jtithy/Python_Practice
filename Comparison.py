print("Comparison of Different Python Concepts")
#Taking input for comparisons
num1 = input("Enter 1st number for comparison: ")
num2 = input("Enter 2nd number for comparison: ")

#Performing comparison operations
print("Greater Than:", int(num1) > int(num2))  # Greater Than Operator
print("Less Than:", int(num1) < int(num2))  # Less Than Operator
print("Greater Than or Equal To:", int(num1) >= int(num2)) # Greater Than or Equal To Operator
print("Less Than or Equal To:", int(num1) <= int(num2)) # Less Than or Equal To Operator
print("Not Equal To:", int(num1) != int(num2)) # Not Equal To Operator
print("Equality:", int(num1) == int(num2)) # Equality Operator